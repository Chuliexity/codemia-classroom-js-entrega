function traductor(word){
    switch(word){
        case 'casa':
            console.log('house')
            break
        case 'mesa':
            console.log('table')
            break
        case 'perro':
            console.log('dog')
            break
        case 'gato':
            console.log('cat')
            break
    }
}

traductor("gato")
traductor("mesa")
traductor("perro")
traductor("hoja")
traductor("casa")